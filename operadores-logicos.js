let nota1 = 7;
let nota2 = 4;
let resultado;

// AND 
resultado = (nota1 > 6) && (nota2 > 6);
console.log(resultado);
// para que el valor de resultado sea verdadero hay q complir con las dos condiciones
// utilizamos la tecla && para definir el AND 

// ORD 
resultado = (nota1 > 6) || (nota2 > 6);
console.log(resultado);
// para que el valor de resultado sea verdadero hay q complir con al menos una condicion
// utilizamos la tecla || para definir el ORD

// not 

resultado = !(nota1 > 6);
console.log(resultado);
// el simbolo ! cambia el valor booleano de la operacion logica